def train_therapy_model(data_file_path, output_dir="therapy_model"):
    """
    Complete pipeline to load, clean, and train a therapy response model.

    Args:
        data_file_path (str): Path to the therapy dataset file
        output_dir (str): Directory to save the trained model
    """

    print("\n" + "="*60)
    print("🧠 THERAPY CHATBOT MODEL TRAINING PIPELINE")
    print("="*60 + "\n")

    # -----------------------------
    # STEP 1: LOAD AND CLEAN DATA
    # -----------------------------
    contexts, responses = load_and_clean_therapy_data(data_file_path)

    if len(contexts) == 0:
        print("❌ No valid data found. Exiting.")
        return

    # -----------------------------
    # STEP 2: CATEGORIZE CONTEXTS
    # -----------------------------
    print("\n🏷️  Categorizing contexts into intents...")
    labels = categorize_mental_health_contexts(contexts)

    # Show distribution of intents
    from collections import Counter
    intent_counts = Counter(labels)
    print("\n📊 Intent Distribution:")
    for intent, count in intent_counts.most_common():
        print(f"   {intent}: {count}")

    # -----------------------------
    # STEP 3: ENCODE LABELS
    # -----------------------------
    print("\n🔢 Encoding labels...")
    label_encoder = LabelEncoder()
    encoded_labels = label_encoder.fit_transform(labels)

    # Save label mapping for later use
    label_map = dict(zip(label_encoder.classes_, range(len(label_encoder.classes_))))
    os.makedirs(output_dir, exist_ok=True)

    with open(f"{output_dir}/label_map.json", "w") as f:
        json.dump(label_map, f, indent=2)
    print(f"✅ Saved label mapping to {output_dir}/label_map.json")

    # Save context-response pairs for retrieval
    training_data = {
        "contexts": contexts,
        "responses": responses,
        "labels": labels
    }
    with open(f"{output_dir}/training_data.json", "w") as f:
        json.dump(training_data, f, indent=2)
    print(f"✅ Saved training data to {output_dir}/training_data.json")

    # -----------------------------
    # STEP 4: SPLIT DATA
    # -----------------------------
    print("\n✂️  Splitting data into train/validation sets...")
    train_texts, val_texts, train_labels, val_labels = train_test_split(
        contexts,
        encoded_labels,
        test_size=0.2,
        random_state=42,
        stratify=encoded_labels
    )
    print(f"   Training samples: {len(train_texts)}")
    print(f"   Validation samples: {len(val_texts)}")

    # -----------------------------
    # STEP 5: TOKENIZATION
    # -----------------------------
    print("\n🔤 Loading tokenizer and tokenizing data...")
    tokenizer = DistilBertTokenizerFast.from_pretrained("distilbert-base-uncased")

    train_encodings = tokenizer(
        train_texts,
        truncation=True,
        padding=True,
        max_length=256  # Longer for therapy contexts
    )
    val_encodings = tokenizer(
        val_texts,
        truncation=True,
        padding=True,
        max_length=256
    )
    print("✅ Tokenization complete")

    # -----------------------------
    # STEP 6: CREATE DATASETS
    # -----------------------------
    print("\n📦 Creating PyTorch datasets...")
    train_dataset = TherapyDataset(train_encodings, train_labels)
    val_dataset = TherapyDataset(val_encodings, val_labels)

    # -----------------------------
    # STEP 7: LOAD MODEL
    # -----------------------------
    print("\n🤖 Loading DistilBERT model...")
    model = DistilBertForSequenceClassification.from_pretrained(
        "distilbert-base-uncased",
        num_labels=len(label_map),
    )
    print(f"✅ Model loaded with {len(label_map)} output classes")

    # -----------------------------
    # STEP 8: CONFIGURE TRAINING
    # -----------------------------
    print("\n⚙️  Configuring training parameters...")
    training_args = TrainingArguments(
        output_dir=output_dir,
        num_train_epochs=10,
        per_device_train_batch_size=8,
        per_device_eval_batch_size=8,
        learning_rate=2e-5,
        weight_decay=0.01,
        warmup_steps=100,
        logging_dir=f"{output_dir}/logs",
        logging_steps=10,
        save_strategy="epoch",
        eval_strategy="epoch",
        load_best_model_at_end=True,
        metric_for_best_model="eval_loss",
        report_to="none",
    )

    # -----------------------------
    # STEP 9: TRAIN MODEL
    # -----------------------------
    print("\n🚀 Starting training...\n")
    print("="*60)

    trainer = Trainer(
        model=model,
        args=training_args,
        train_dataset=train_dataset,
        eval_dataset=val_dataset,
    )

    trainer.train()

    # -----------------------------
    # STEP 10: SAVE MODEL
    # -----------------------------
    print("\n💾 Saving trained model...")
    model.save_pretrained(output_dir)
    tokenizer.save_pretrained(output_dir)

    # Final evaluation
    final_metrics = trainer.evaluate()
    print("\n" + "="*60)
    print("✅ TRAINING COMPLETE!")
    print("="*60)
    print(f"📊 Final Validation Loss: {final_metrics['eval_loss']:.4f}")
    print(f"📁 Model saved to: {output_dir}/")
    print("\nFiles created:")
    print(f"   - {output_dir}/pytorch_model.bin (model weights)")
    print(f"   - {output_dir}/config.json (model config)")
    print(f"   - {output_dir}/label_map.json (intent labels)")
    print(f"   - {output_dir}/training_data.json (for response retrieval)")
    print("="*60 + "\n")


