def clean_text(text):
    """
    Clean text by removing extra whitespace, special characters, and normalizing.

    Args:
        text (str): Raw text to clean

    Returns:
        str: Cleaned text
    """
    if not text or not isinstance(text, str):
        return ""

    # Remove extra whitespace and newlines
    text = re.sub(r'\s+', ' ', text)

    # Remove leading/trailing whitespace
    text = text.strip()

    # Remove special unicode characters that might cause issues
    text = text.encode('ascii', 'ignore').decode('ascii')

    # Normalize quotes
    text = text.replace('\u2019', "'").replace('\u201c', '"').replace('\u201d', '"')

    return text


def load_and_clean_therapy_data(file_path):
    """
    Load therapy dataset from JSONL format and clean it.

    Expected format: Each line is a JSON object with "Context" and "Response" keys

    Args:
        file_path (str): Path to the therapy dataset file

    Returns:
        tuple: (contexts, responses) - lists of cleaned contexts and responses
    """
    contexts = []
    responses = []

    print(f"📂 Loading data from {file_path}...")

    with open(file_path, 'r', encoding='utf-8') as f:
        for line_num, line in enumerate(f, 1):
            try:
                # Parse JSON line
                data = json.loads(line.strip())

                # Extract context and response
                context = data.get('Context', '')
                response = data.get('Response', '')

                # Clean the texts
                context = clean_text(context)
                response = clean_text(response)

                # Skip if either is empty
                if context and response:
                    contexts.append(context)
                    responses.append(response)
                else:
                    print(f"⚠️  Skipping line {line_num}: Empty context or response")

            except json.JSONDecodeError:
                print(f"⚠️  Skipping line {line_num}: Invalid JSON")
                continue
            except Exception as e:
                print(f"⚠️  Error on line {line_num}: {str(e)}")
                continue

    print(f"✅ Loaded {len(contexts)} valid context-response pairs")
    return contexts, responses


def categorize_mental_health_contexts(contexts):
    """
    Automatically categorize mental health contexts into intent categories
    based on keywords and themes.

    Args:
        contexts (list): List of therapy context strings

    Returns:
        list: List of intent labels for each context
    """
    labels = []

    # Define keyword patterns for different mental health intents
    intent_patterns = {
        'depression': ['depress', 'sad', 'hopeless', 'worthless', 'empty', 'down', 'unmotivated'],
        'anxiety': ['anxious', 'worry', 'nervous', 'panic', 'stress', 'fear', 'overwhelm'],
        'sleep_issues': ['sleep', 'insomnia', 'tired', 'exhausted', 'rest', 'fatigue'],
        'self_esteem': ['worthless', 'confidence', 'self-esteem', 'value', 'myself', 'identity'],
        'relationship': ['relationship', 'partner', 'marriage', 'boyfriend', 'girlfriend', 'divorce'],
        'family': ['family', 'parents', 'mother', 'father', 'siblings', 'children'],
        'suicide': ['suicid', 'kill myself', 'end it', 'die', 'death wish'],
        'trauma': ['trauma', 'abuse', 'ptsd', 'flashback', 'assault'],
        'grief': ['grief', 'loss', 'died', 'death', 'mourning', 'bereave'],
        'anger': ['angry', 'rage', 'furious', 'irritated', 'frustrated'],
        'general_support': []  # Default category
    }

    for context in contexts:
        context_lower = context.lower()
        matched_intent = 'general_support'  # Default

        # Check each intent pattern
        for intent, keywords in intent_patterns.items():
            if any(keyword in context_lower for keyword in keywords):
                matched_intent = intent
                break  # Use first match

        labels.append(matched_intent)

    return labels


