def predict_therapy_response(text, model_dir="therapy_model"):
    """
    Predict intent and retrieve appropriate response for user input.

    Args:
        text (str): User's input message
        model_dir (str): Directory containing the trained model

    Returns:
        tuple: (intent, response)
    """
    import random

    # Load model and tokenizer
    model = DistilBertForSequenceClassification.from_pretrained(model_dir)
    tokenizer = DistilBertTokenizerFast.from_pretrained(model_dir)

    # Load label mapping
    with open(f"{model_dir}/label_map.json", "r") as f:
        label_map = json.load(f)

    # Load training data for response retrieval
    with open(f"{model_dir}/training_data.json", "r") as f:
        training_data = json.load(f)

    # Predict
    inputs = tokenizer(text, return_tensors="pt", truncation=True, padding=True, max_length=256)

    with torch.no_grad():
        outputs = model(**inputs)

    prediction = torch.argmax(outputs.logits, dim=1).item()

    # Get intent
    intent = [k for k, v in label_map.items() if v == prediction][0]

    # Find matching responses
    matching_responses = [
        training_data["responses"][i]
        for i, label in enumerate(training_data["labels"])
        if label == intent
    ]

    # Return random response from matching category
    response = random.choice(matching_responses) if matching_responses else "I'm here to listen and support you."

    return intent, response


