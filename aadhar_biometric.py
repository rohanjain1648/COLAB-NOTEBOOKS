    Q3 = df_master[col].quantile(0.75)
    IQR = Q3 - Q1
    lower_bound = Q1 - 1.5 * IQR
    upper_bound = Q3 + 1.5 * IQR

    outliers = df_master[(df_master[col] < lower_bound) | (df_master[col] > upper_bound)]

    print(f"\nColumn: {col}")
    print(f"Q1 (25th percentile): {Q1}")
